 /*************************************************************************/
 //---------------------------  bubble_sort( )  --------------------------//
 /*************************************************************************/

 void bubble_sort(int array[],const int size)
 {
    for(int i=0;i<(size-1);i++)
    {
       for(int j=(size-1);j>=(i+1);j--)
       {
	  if(array[(j-1)]>array[j])
	  {
	     int temp=array[(j-1)];

	     array[(j-1)]=array[j];
	     array[j]=temp;
	  }
       }
    }
 }

